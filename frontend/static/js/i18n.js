function getLocale() {
    let match = document.cookie.match(new RegExp('(^| )sol_locale=([^;]+)'));
    if (match) return match[2];
    return localStorage.getItem('sol_pref_lang') || document.documentElement.lang || "en";
}

function translate(key) {
    const locale = getLocale();
    return i18nDict[locale]?.[key] || i18nDict["en"]?.[key] || key;
}

function applyTranslations(lang) {
    if (!lang) lang = getLocale();
    document.cookie = `sol_locale=${lang}; path=/; max-age=31536000; SameSite=Lax`;
    localStorage.setItem('sol_pref_lang', lang);
    document.documentElement.setAttribute('lang', lang);
    if (lang === 'ar') {
        document.documentElement.setAttribute('dir', 'rtl');
    } else {
        document.documentElement.setAttribute('dir', 'ltr');
    }
    
    // Find all elements with data-i18n attribute
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = i18nDict[lang]?.[key] || i18nDict["en"]?.[key];
        if (translation) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                if (el.hasAttribute('placeholder')) {
                    el.setAttribute('placeholder', translation);
                } else {
                    el.value = translation;
                }
            } else {
                el.innerHTML = translation;
            }
        }
    });

    // Update switcher state/text in DOM if switcher exists
    const switcherText = document.getElementById('lang-switch-text');
    if (switcherText) {
        switcherText.textContent = lang === 'ar' ? 'English' : 'العربية';
    }
}

window.translate = translate;
window.getLocale = getLocale;
window.applyTranslations = applyTranslations;
